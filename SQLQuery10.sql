 (           
   NUM_SEC_ARCHIVO INT,          
   NUM_TRANSFERENCIA INT,           
   NUM_DETALLE INT,          
   NUM_SEC_LOTE INT,          
   NUM_SEC_REGISTRO INT,             
   SECUENCIA CHAR(7) ,        
   ID_FORMA INT,         
   TIP_TRANSFERENCIA INT,        
   PRIMARY KEY(NUM_SEC_ARCHIVO, NUM_TRANSFERENCIA, secuencia, ID_FORMA,TIP_TRANSFERENCIA)         
 )             
