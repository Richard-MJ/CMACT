 INSERT INTO @LTBL_MOVIMIENTO_ARCHIVO        
 SELECT CA.NUM_SEC_ARCHIVO, TDC.NUM_TRANSFERENCIA, TDC.NUM_DETALLE, CA.NUM_SEC_LOTE, CA.NUM_SEC_REGISTRO, CA.SECUENCIA  , 1, ENC.TIP_TRANSFERENCIA        
 FROM @LTBL_CABECERA_DETALLE CA          
  inner JOIN  CC.CC_TRANSFERENCIAS_DETALLE_CCE TDC (NOLOCK) ON               
      CA.SECUENCIA =         
  CASE WHEN LEN(TDC.NUM_TRANSFERENCIA)<=4 THEN RIGHT('0000000' + CONVERT(VARCHAR(10),TDC.NUM_TRANSFERENCIA) + RIGHT('000' +  CONVERT(VARCHAR(10),TDC.NUM_DETALLE),3)  ,7)        
 WHEN LEN(TDC.NUM_TRANSFERENCIA)=5 THEN RIGHT('0000000' + CONVERT(VARCHAR(10),TDC.NUM_TRANSFERENCIA) + RIGHT('00' +  CONVERT(VARCHAR(10),TDC.NUM_DETALLE),2) ,7)        
 WHEN LEN(TDC.NUM_TRANSFERENCIA)=6 THEN RIGHT('0000000' + CONVERT(VARCHAR(10),TDC.NUM_TRANSFERENCIA) + RIGHT('0' +  CONVERT(VARCHAR(10),TDC.NUM_DETALLE),1) ,7)        
  WHEN LEN(TDC.NUM_TRANSFERENCIA)>=7 THEN RIGHT('0000000' + CONVERT(VARCHAR(10),TDC.NUM_TRANSFERENCIA) + RIGHT('' +  CONVERT(VARCHAR(10),TDC.NUM_DETALLE),0) ,7)         
  END        
         
  INNER JOIN CC.CC_TRANSFERENCIAS_ENCABEZADO ENC (NOLOCK) ON TDC.NUM_TRANSFERENCIA = ENC.NUM_TRANSFERENCIA AND ENC.TIP_TRANSFERENCIA != 'HEC' --apereira ticket 28237         
  INNER JOIN CF.CF_TIN_TIPO_TRANSFERENCIA T (NOLOCK) ON T.COD_TIN_TIPO_TRANSFERENCIA=ENC.TIP_TRANSFERENCIA AND T.IND_ESTADO='A'        
       
