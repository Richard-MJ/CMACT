WITH TBL_REGISTROS AS (
	SELECT
		NUM_SEC_RESPUESTA,
		NUM_SEC_ARCHIVO,
		DET_REGISTRO,
		Tipo = 
			CASE 
				WHEN DET_REGISTRO LIKE '%FECHA%' THEN 'FECHA'
				WHEN DET_REGISTRO LIKE '%HORA%' THEN 'HORA'
				ELSE NULL
			END,
		ROW_NUMBER() OVER (PARTITION BY NUM_SEC_ARCHIVO ORDER BY NUM_SEC_RESPUESTA) AS RN
	FROM BA.BA_TIN_DETALLE_ARCHIVO_RESPUESTA
	WHERE DET_REGISTRO LIKE '%FECHA%' OR DET_REGISTRO LIKE '%HORA%'
),
TBL_FECHAS AS (
	SELECT * FROM TBL_REGISTROS WHERE Tipo = 'FECHA'
),
TBL_HORAS AS (
	SELECT * FROM TBL_REGISTROS WHERE Tipo = 'HORA'
),
TBL_ARCHIVO AS (
	SELECT
		f.NUM_SEC_ARCHIVO,
		FECHA_HORA = CONVERT(DATETIME,
			LTRIM(RTRIM(SUBSTRING(f.DET_REGISTRO, CHARINDEX('FECHA', f.DET_REGISTRO) + 9, 11))) + ' ' +
			LTRIM(RTRIM(SUBSTRING(h.DET_REGISTRO, CHARINDEX('HORA', h.DET_REGISTRO) + 12, 8)))
		, 103)
	FROM TBL_FECHAS f
	JOIN TBL_HORAS h
		ON f.NUM_SEC_ARCHIVO = h.NUM_SEC_ARCHIVO
		AND h.RN = f.RN + 1
)
SELECT 
	CA.NUM_SEC_ARCHIVO,
	FEC_OPERACION = MOV.FEC_TRANSACCION,           
	TBL.FECHA_HORA AS FECHA_HORA,
	ESTADO_TRANSFERENCIA = CASE TE.IND_ESTADO WHEN 'A' THEN 'APLICADO'  WHEN 'P' THEN 'PENDIENTE' WHEN 'E' THEN 'ENVIADO' WHEN 'N' THEN 'ANULADO' WHEN 'D' THEN 'DEVOLUCION' END ,          
	ESTADO_ARCHIVO = CASE CA.IND_ESTADO WHEN 'G' THEN 'GENERADO' WHEN 'R' THEN 'RECIBIDO' WHEN 'E' THEN 'ERROR' WHEN 'N' THEN 'ANULADO' END,
	DATEDIFF(SECOND, MOV.FEC_TRANSACCION, TBL.FECHA_HORA) AS VAL_SEGUNDOS
 FROM CC.CC_TRANSFERENCIAS_ENCABEZADO TE (NOLOCK)          
 INNER JOIN @LTBL_MOVIMIENTO_FINAL MOV
  ON TE.NUM_MOVIMIENTO = MOV.NUM_MOVIMIENTO          
 INNER JOIN CC.CC_TRANSFERENCIAS_DETALLE_CCE TDC (NOLOCK)           
  ON TE.NUM_TRANSFERENCIA = TDC.NUM_TRANSFERENCIA AND TE.TIP_TRANSFERENCIA != 'HEC'        
  INNER JOIN @LTBL_MOVIMIENTO_ARCHIVO3 MOVD          
   ON TDC.NUM_TRANSFERENCIA = MOVD.NUM_TRANSFERENCIA AND TDC.NUM_DETALLE = MOVD.NUM_DETALLE
   AND TE.TIP_TRANSFERENCIA = MOVD.TIP_TRANSFERENCIA        
   INNER JOIN BA.BA_TIN_REGISTRO_INDIVIDUAL RI (NOLOCK)           
    ON MOVD.NUM_SEC_ARCHIVO = RI.NUM_SEC_ARCHIVO AND RI.NUM_SEC_REGISTRO = MOVD.NUM_SEC_REGISTRO          
     AND MOVD.NUM_SEC_LOTE = RI.NUM_SEC_LOTE AND MOVD.NUM_SEC_REGISTRO = RI.NUM_SEC_REGISTRO         
  AND ((RI.NUM_CUENTA_ACREDITAR = (CASE WHEN TE.TIP_TRANSFERENCIA = 220 THEN TDC.NUM_CUENTA_CCI END )) OR         
  (RI.NUM_CUENTA_ACREDITAR != (CASE WHEN TE.TIP_TRANSFERENCIA != 220 THEN TDC.NUM_CUENTA_CCI END ) ))         
     INNER JOIN BA.BA_TIN_CABECERA_ARCHIVO CA (NOLOCK)          
      ON RI.NUM_SEC_ARCHIVO = CA.NUM_SEC_ARCHIVO          
     INNER JOIN BA.BA_TIN_CABECERA_LOTE BL (NOLOCK)           
      ON RI.NUM_SEC_ARCHIVO = BL.NUM_SEC_ARCHIVO AND RI.NUM_SEC_LOTE = BL.NUM_SEC_LOTE           
     INNER JOIN BA.BA_TIN_REGISTRO_ADICIONAL RA (NOLOCK)           
      ON RI.NUM_SEC_ARCHIVO = RA.NUM_SEC_ARCHIVO AND RI.NUM_SEC_LOTE = RA.NUM_SEC_LOTE           
       AND RI.NUM_SEC_REGISTRO = RA.NUM_SEC_REGISTRO        
 INNER JOIN CL.CL_CLIENTES CL (NOLOCK)          
  ON TE.COD_CLIENTE = CL.COD_CLIENTE          
 INNER JOIN CC.CC_CUENTA_EFECTIVO CE (NOLOCK)          
  ON TE.NUM_CUENTA = CE.NUM_CUENTA         
  AND CE.COD_EMPRESA = '1'        
  INNER JOIN CF.CF_MONEDAS MO (NOLOCK)          
   ON CE.COD_MONEDA = MO.COD_MONEDA          
  INNER JOIN CF.CF_AGENCIAS AG1 (NOLOCK)          
   ON CE.COD_AGENCIA = AG1.COD_AGENCIA          
   AND AG1.COD_EMPRESA = '1'        
 INNER JOIN BA.BA_ENTIDAD_FINANCIERA_CCE EF (NOLOCK)          
  ON EF.ID_ENTIDAD = TDC.ID_ENTIDAD        
 INNER JOIN CF.CF_TIN_TIPO_TRANSFERENCIA TT  (NOLOCK)           
  ON TE.TIP_TRANSFERENCIA = TT.COD_TIN_TIPO_TRANSFERENCIA
 LEFT JOIN CL.CL_TIPOS_ID TI (NOLOCK)          
  ON TDC.COD_TIPO_DOCUMENTO = TI.COD_TIPO_ID_CCE          
  AND TI.COD_EMPRESA = '1'
 INNER JOIN CF.CF_AGENCIAS AG2 (NOLOCK)
  ON TE.COD_AGENCIA = AG2.COD_AGENCIA
 INNER JOIN CG.CG_ASTO_RESUMEN AR (NOLOCK)
  ON MOV.NUM_ASIENTO = AR.NUM_ASIENTO
  INNER JOIN CG.CG_ESTADOS_ASIENTO EAC (NOLOCK)          
   ON AR.EST_ASIENTO = EAC.COD_ESTADO
 INNER JOIN TBL_ARCHIVO AS TBL ON TBL.NUM_SEC_ARCHIVO = CA.NUM_SEC_ARCHIVO 
 WHERE TE.IND_ESTADO IN ('E')
ORDER BY FEC_OPERACION, CE.COD_MONEDA, NUM_SECUENCIA ASC