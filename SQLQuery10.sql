DECLARE @ADT_FEC_INICIO DATETIME = '20250101';
DECLARE @ADT_FEC_FIN DATETIME = '20250730';

SET @ADT_FEC_INICIO = CONVERT(DATETIME,@ADT_FEC_INICIO)          
SET @ADT_FEC_FIN = DATEADD(ms,-3, CONVERT(DATETIME,  DATEADD(DD,1,CONVERT(DATE,  @ADT_FEC_FIN))))          
                 
 DECLARE @LTBL_CABECERA_ARCHIVO TABLE              
 (  NUM_SEC_ARCHIVO INT,          
   PRIMARY KEY(NUM_SEC_ARCHIVO)          
 )             
 INSERT INTO @LTBL_CABECERA_ARCHIVO          
 SELECT CA.NUM_SEC_ARCHIVO FROM BA.BA_TIN_CABECERA_ARCHIVO CA              
  WHERE CONVERT(DATE,CA.FEC_PRESENTACION) BETWEEN @ADT_FEC_INICIO AND @ADT_FEC_FIN          
   AND CA.COD_APLICACION LIKE '%'           
   AND CA.TIPO_SESION = '1'          
   AND NOM_ARCHIVO LIKE 'EC%'        
                 
 DECLARE @LTBL_CABECERA_DETALLE TABLE              
 (  NUM_SEC_ARCHIVO INT,           
   NUM_SEC_LOTE INT,          
   NUM_SEC_REGISTRO INT,          
   SECUENCIA CHAR(7),          
   PRIMARY KEY(NUM_SEC_ARCHIVO, NUM_SEC_LOTE, NUM_SEC_REGISTRO)          
 )             
 INSERT INTO @LTBL_CABECERA_DETALLE          
 SELECT CA.NUM_SEC_ARCHIVO, RI.NUM_SEC_LOTE, RI.NUM_SEC_REGISTRO, RI.SECUENCIA         
 FROM @LTBL_CABECERA_ARCHIVO CA            
  INNER JOIN BA.BA_TIN_REGISTRO_INDIVIDUAL RI (NOLOCK) ON CA.NUM_SEC_ARCHIVO = RI.NUM_SEC_ARCHIVO          
            
