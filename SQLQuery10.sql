INNER JOIN CC.CC_TRANSFERENCIAS_DETALLE_CCE DET (NOLOCK) ON DET.ID_DETALLE = CA.SECUENCIA        
INNER JOIN CC.CC_TRANSFERENCIAS_ENCABEZADO ENC (NOLOCK) ON  ENC.NUM_TRANSFERENCIA = DET.NUM_TRANSFERENCIA AND ENC.TIP_TRANSFERENCIA != 'HEC'    
INNER JOIN CF.CF_TIN_TIPO_TRANSFERENCIA T (NOLOCK) ON T.COD_TIN_TIPO_TRANSFERENCIA=ENC.TIP_TRANSFERENCIA AND T.IND_ESTADO='A'        
where ma.SECUENCIA is null        
        
