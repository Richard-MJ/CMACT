 INSERT INTO @LTBL_MOVIMIENTO          
 SELECT TE.NUM_TRANSFERENCIA, M.NUM_MOVIMIENTO, M.COD_AGENCIA, M.TIP_TRANSACCION, M.SUBTIP_TRANSAC, M.COD_PRODUCTO,          
  M.NUM_ASIENTO, M.FEC_MOVIMIENTO, MON_ITF = ISNULL(M2.MON_MOVIMIENTO,0), TT.COD_TIN_TIPO_TRANSFERENCIA,    
  DE.ID_DETALLE              
  FROM CC.CC_MOVIMTO_DIARIO M (NOLOCK)          
   INNER JOIN CC.CC_TRANSFERENCIAS_ENCABEZADO TE (NOLOCK) ON M.NUM_MOVIMIENTO = TE.NUM_MOVIMIENTO          
   INNER JOIN CC.CC_TRANSFERENCIAS_DETALLE_CCE DE (NOLOCK) ON TE.NUM_TRANSFERENCIA = DE.NUM_TRANSFERENCIA            
    INNER JOIN CF.CF_TIN_TIPO_TRANSFERENCIA TT  (NOLOCK) ON TE.TIP_TRANSFERENCIA = TT.COD_TIN_TIPO_TRANSFERENCIA          
   LEFT JOIN CC.CC_MOVIMTO_DIARIO M2 (NOLOCK) ON M.NUM_MOVIMIENTO = M2.NUM_MOV_FUENTE AND M2.EST_MOVIMIENTO = 'A' AND M2.TIP_TRANSACCION = '203'          
   INNER JOIN (SELECT DISTINCT NUM_TRANSFERENCIA, TIP_TRANSFERENCIA FROM @LTBL_MOVIMIENTO_ARCHIVO3) TD ON TD.NUM_TRANSFERENCIA = TE.NUM_TRANSFERENCIA AND TD.TIP_TRANSFERENCIA = TE.TIP_TRANSFERENCIA        
  WHERE          
  M.EST_MOVIMIENTO <> 'N' AND TE.IND_ESTADO <> 'N'          
 UNION ALL          
 SELECT TE.NUM_TRANSFERENCIA, M.NUM_MOVIMIENTO, M.COD_AGENCIA, M.TIP_TRANSACCION, M.SUBTIP_TRANSAC, M.COD_PRODUCTO,          
  M.NUM_ASIENTO, M.FEC_MOVIMIENTO, MON_ITF = ISNULL(M2.MON_MOVIMIENTO,0), TT.COD_TIN_TIPO_TRANSFERENCIA,    
  DE.ID_DETALLE              
  FROM CC.CC_MOVIMTO_MENSUAL M (NOLOCK)          
   INNER JOIN CC.CC_TRANSFERENCIAS_ENCABEZADO TE (NOLOCK) ON M.NUM_MOVIMIENTO = TE.NUM_MOVIMIENTO          
      INNER JOIN CC.CC_TRANSFERENCIAS_DETALLE_CCE DE (NOLOCK) ON TE.NUM_TRANSFERENCIA = DE.NUM_TRANSFERENCIA         
    INNER JOIN CF.CF_TIN_TIPO_TRANSFERENCIA TT  (NOLOCK) ON TE.TIP_TRANSFERENCIA = TT.COD_TIN_TIPO_TRANSFERENCIA          
   LEFT JOIN CC.CC_MOVIMTO_MENSUAL M2 (NOLOCK) ON M.NUM_MOVIMIENTO = M2.NUM_MOV_FUENTE AND M2.EST_MOVIMIENTO = 'A' AND M2.TIP_TRANSACCION = '203'          
      INNER JOIN (SELECT DISTINCT NUM_TRANSFERENCIA, TIP_TRANSFERENCIA FROM @LTBL_MOVIMIENTO_ARCHIVO3) TD ON TD.NUM_TRANSFERENCIA = TE.NUM_TRANSFERENCIA AND TD.TIP_TRANSFERENCIA= TE.TIP_TRANSFERENCIA        
WHERE M.EST_MOVIMIENTO <> 'N' AND TE.IND_ESTADO <> 'N'           
                 
